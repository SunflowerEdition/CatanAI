class Player:
    def __init__(self):
        self.num_wool = 0
        self.num_brick = 0
        self.num_lumber = 0
        self.num_ore = 0
        self.num_grain = 0

    def reset(self):
        self.num_wool = 0
        self.num_brick = 0
        self.num_lumber = 0
        self.num_ore = 0
        self.num_grain = 0